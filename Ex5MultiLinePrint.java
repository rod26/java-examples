public class Ex5MultiLinePrint {
    public static void main(String[] args) {
        // Print multiple lines using \n
        System.out.println("This is the first line.\nThis is the second line.\nThis is the third line.");
        
        // Print multiple lines using multiple println statements
        System.out.println("This is the first line.");
        System.out.println("This is the second line.");
        System.out.println("This is the third line.");
    }
}