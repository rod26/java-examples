public class Ex17ComparisonExample {
    public static void main(String[] args) {
        int a = 5;
        int b = 5;
        
        // Greater than or equal to
        boolean isAGreaterThanOrEqualToB = (a >= b);  // true
        
        // Less than or equal to
        boolean isALessThanOrEqualToB = (a <= b);  // true
        
        System.out.println("a >= b: " + isAGreaterThanOrEqualToB);  // Output: a >= b: true
        System.out.println("a <= b: " + isALessThanOrEqualToB);     // Output: a <= b: true
    }
}
